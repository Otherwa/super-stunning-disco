// send signup email
function sendEmail(email) {

  var mailOptions = {
    from: process.env.EMAIL,
    to: email,
    subject: 'Thanks For Registering',
    text: 'Thanks For Registering',
    html:
      `
      Welcome
      `
  }
  transporter.sendMail(mailOptions, function (error, info) {
    if (error) {
      console.log(error)
    } else {
      console.log('Email sent: ' + info.response)
    }
  })
}


